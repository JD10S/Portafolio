<script>
  document.getElementById('contactForm').addEventListener('submit', async function(e) {
        e.preventDefault();

        const form = e.target;
        const button = form.querySelector('button[type="submit"]');
        const originalText = button.innerHTML;

      
        document.querySelectorAll('.error-name, .error-email, .error-message').forEach(el => el.textContent = '');

        button.disabled = true;
        button.innerHTML = 'Enviando...';

        const formData = new FormData(form);

        try {
            const response = await fetch("{{ route('contact.send') }}", {
                method: 'POST',
                body: formData,
                headers: {
                    'X-Requested-With': 'XMLHttpRequest',
                    'X-CSRF-TOKEN': document.querySelector('meta[name="csrf-token"]').getAttribute('content'), // ← ESTA LÍNEA ES LA CLAVE
                    'Accept': 'application/json'
                }
            });

            const data = await response.json();
          
            if (!response.ok) {
                const text = await response.text();
                console.error('Error HTTP:', response.status, text);
                throw new Error('Error del servidor');
            }


            if (response.ok) {
                form.reset();
                showModal();
            } else {
                if (data.errors) {
                    Object.keys(data.errors).forEach(key => {
                        const errorEl = document.querySelector(`.error-${key}`);
                        if (errorEl) errorEl.textContent = data.errors[key][0];
                    });
                }
            }
        } catch (error) {
            console.error('Error en fetch:', error);
            
        } finally {
            button.disabled = false;
            button.innerHTML = originalText;
        }
    });

    function showModal() {
        document.getElementById('successModal').classList.remove('hidden');
    }

    function closeModal() {
        document.getElementById('successModal').classList.add('hidden');
    }
</script>